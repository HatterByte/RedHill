def map_to_official_category(
    result: Union[Dict[str, any], ClassificationResult], 
    intent_category_map: Dict[str, Tuple[str, str]],
    openai_client: Any
) -> Dict[str, any]:
    """Map categories to official ones using GPT"""
    
    # Convert ClassificationResult to dict if needed
    if isinstance(result, ClassificationResult):
        result_dict = {
            "category": result.category,
            "subcategory": result.subcategory,
            "severity": result.severity,
            "confidence": result.confidence,
            "reason": result.reason,
            "intent": result.intent
        }
    else:
        result_dict = result

    # Create list of valid categories and subcategories
    valid_categories = {}
    for _, (category, subcategory) in intent_category_map.items():
        if category not in valid_categories:
            valid_categories[category] = []
        if subcategory not in valid_categories[category]:
            valid_categories[category].append(subcategory)

    # Create the prompt
    prompt = f"""Given this complaint classification:
Category: {result_dict['category']}
Subcategory: {result_dict['subcategory']}
Description: {result_dict['reason']}

Map it to the EXACT category and subcategory from this official list (do not modify or create new categories):

{json.dumps(valid_categories, indent=2)}

Return ONLY a JSON with these exact keys:
{{
    "category": "exact category from list",
    "subcategory": "exact subcategory from list",
    "reason": "brief explanation of mapping"
}}"""

    # Get GPT's mapping
    response = openai_client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {"role": "system", "content": "You are a railway complaint classification system that strictly maps to official categories."},
            {"role": "user", "content": prompt}
        ],
        temperature=0,
        max_tokens=200
    )

    try:
        mapped = json.loads(response.choices[0].message.content)
        
        # Update with mapped categories
        result_dict['category'] = mapped['category']
        result_dict['subcategory'] = mapped['subcategory']
        result_dict['reason'] = mapped['reason']
        
        # Update intent based on new category/subcategory
        for intent, (cat, subcat) in intent_category_map.items():
            if cat == mapped['category'] and subcat == mapped['subcategory']:
                result_dict['intent'] = intent
                break
                
        return result_dict
    except (json.JSONDecodeError, KeyError) as e:
        # Fallback to Miscellaneous
        return {
            "category": "Miscellaneous",
            "subcategory": "Miscellaneous",
            "severity": result_dict.get('severity', 0.5),
            "confidence": result_dict.get('confidence', 0.5),
            "reason": "Failed to map to official category",
            "intent": "miscellaneous"
        }


