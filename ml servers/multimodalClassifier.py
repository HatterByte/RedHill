from similarityFinder import SimilarityFinder
from typing import List, Dict, Union, Optional, Any, Tuple

@dataclass
class ClassificationResult:
    """Data class for storing classification results"""
    category: str
    subcategory: str
    severity: float
    confidence: str
    reason: str
    intent: str

class IntentBasedMultimodalClassifier:
    def __init__(
        self,
        openai_api_key: str,
        similarity_data_path: str = "data2.json",
        vision_model_name: str = "google/vit-base-patch16-224",
        device: Optional[str] = None
    ):
        openai.api_key = openai_api_key
        self.similarity_finder = SimilarityFinder(
            data_path=similarity_data_path,
            model_name=vision_model_name,
            device=device
        )
        self.categories = self._get_category_descriptions()
        self.intent_examples = self._load_intent_examples()
        self.valid_categories = self._get_valid_categories()
        
    def _get_valid_categories(self) -> Dict[str, List[str]]:
        """Create a mapping of valid categories and their subcategories"""
        valid_cats = {}
        for category, info in self.categories.items():
            valid_cats[category] = list(info['subcategories'].keys())
        return valid_cats

    def generate_classification_prompt(self, text: str, visual_results: List[Dict]) -> str:
        """Generate a detailed prompt with strict category enforcement"""
        visual_analysis = "\n".join([f"- {result['description']} (confidence: {result['similarity_score']})"
                                   for result in visual_results])
        
        # Create categories JSON string
        categories_json = json.dumps(self.valid_categories, indent=2)
        
        prompt = f"""You are an Indian Railways complaint analysis system. You MUST ONLY use the exact category and subcategory names from the official list below.

VALID CATEGORIES AND SUBCATEGORIES:
{categories_json}

INCIDENT INFORMATION:
Text Complaint: {text}

Visual Analysis Results:
{visual_analysis}

ANALYSIS RULES:
1. You MUST choose categories and subcategories ONLY from the provided list
2. For cleanliness issues:
   - Use "Coach-Cleanliness" category
   - Choose appropriate subcategory: "Toilet", "Washbasin", "Coach Interior", etc.
3. Calculate severity (High, Medium , Low):
   - High: Security and Medical Issue
   - Medium: Punctuality , Cleanliness and all those issue
   - Low: Coach maintainence type issue

RESPOND WITH THIS EXACT JSON FORMAT:
{{
    "category": "exact category from list",
    "subcategory": "exact subcategory from list",
    "severity": float between 0-1,
    "confidence": float between 0-1,
    "reason": "brief explanation of classification",
    "intent": "key intent identified"
}}"""

        return prompt

    def validate_category(self, category: str, subcategory: str) -> bool:
        """Validate that category and subcategory are from official list"""
        if category not in self.valid_categories:
            return False
        if subcategory not in self.valid_categories[category]:
            return False
        return True
    
    def classify_content(
        self,
        text: Optional[str] = None,
        images: Optional[List[str]] = None,
        videos: Optional[List[str]] = None,
        model: str = "gpt-4",
        confidence_threshold: float = 0.5
    ) -> ClassificationResult:
        """Classify complaint with strict category validation"""
        visual_results = []
        if images or videos:
            visual_results = self.similarity_finder.find_similar_content(
                images=images,
                videos=videos,
                k=3,
                threshold=confidence_threshold
            )
        
        prompt = self.generate_classification_prompt(
            text if text else "No text provided",
            visual_results
        )
        
        response = openai.chat.completions.create(
            model=model,
            messages=[
                {"role": "system", "content": "You are a precise railway complaint classification system that returns only valid JSON with official categories."},
                {"role": "user", "content": prompt}
            ],
            temperature=0.3,
            max_tokens=500
        )
        
        try:
            result = json.loads(response.choices[0].message.content)
            
            # Validate categories
            if not self.validate_category(result['category'], result['subcategory']):
                # Fallback to most appropriate valid category
                if 'toilet' in text.lower() or 'washbasin' in text.lower():
                    return ClassificationResult(
                        category="Coach-Cleanliness",
                        subcategory="Toilet" if 'toilet' in text.lower() else "Washbasin",
                        severity=float(result['severity']),
                        confidence=float(result['confidence']),
                        reason=result['reason'],
                        intent=result['intent']
                    )
                return self._get_fallback_classification()
            
            return ClassificationResult(
                category=result['category'],
                subcategory=result['subcategory'],
                severity=float(result['severity']),
                confidence=float(result['confidence']),
                reason=result['reason'],
                intent=result['intent']
            )
        except Exception as e:
            return self._get_fallback_classification()

    def _get_fallback_classification(self) -> ClassificationResult:
        """Return a safe fallback classification"""
        return ClassificationResult(
            category="Miscellaneous",
            subcategory="Miscellaneous",
            severity=0.5,
            confidence=0.5,
            reason="Could not determine specific category",
            intent="general_complaint"
        )

